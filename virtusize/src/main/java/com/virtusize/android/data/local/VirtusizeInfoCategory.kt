package com.virtusize.android.data.local

/**
 * This enum contains all the possible info categories that will be displayed in the Product Details tab of the Virtusize web app
 */
enum class VirtusizeInfoCategory(val value: String) {
    MODEL_INFO("modelInfo"),
    GENERAL_FIT("generalFit"),
    BRAND_SIZING("brandSizing"),
    MATERIAL("material"),
}
